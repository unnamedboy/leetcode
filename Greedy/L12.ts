function intToRoman(num: number): string {
    // 定义一个数组，包含所有罗马数字符号和对应的整数值，按从大到小排序
    // 这样贪心时，先用最大单位，保证转换结果标准且字符数最少
    const romanMap: [number, string][] = [
        [1000, "M"],
        [900, "CM"],
        [500, "D"],
        [400, "CD"],
        [100, "C"],
        [90, "XC"],
        [50, "L"],
        [40, "XL"],
        [10, "X"],
        [9, "IX"],
        [5, "V"],
        [4, "IV"],
        [1, "I"]
    ];

    let result = "";
    
    /*
        用哈希映射（hashmap）存储整数与罗马数字的对应关系，并在外层循环遍历这个映射表，是因为：
        需要按照从大到小顺序“贪心”地选择罗马符号对应的数值，用“最大的符号优先”来减去输入整数，保证结果标准且字符最少。
        哈希映射存的是固定的、少量的映射对，存放在外层是为了结构清晰，方便整体依次处理每个符号和值的组合。
        如果将哈希映射放在内层循环，会重复重复创建映射结构，效率低且不直观。
        外层循环遍历映射表让代码逻辑更具可读性，体现按顺序尝试“消除”num的过程，内层循环只做减法和字符串拼接。
        在外层准备映射表也便于后续维护，比如添加特殊组合（如900、400等），代码扩展性好。
    */
    for (const [value, symbol] of romanMap) {
        // 当num还大于等于当前值时，持续减去该值并拼接对应符号
        // 这体现了贪心策略：用最大单位“优先消耗”num，保证生成最简罗马数字
        while (num >= value) {
            result += symbol;
            num -= value;
        }
        // 如果num减到0了，提前结束循环，提升效率
        if (num === 0) break;
    }
    
    // 返回拼接好的罗马数字字符串
    return result;
}

